			</div>
		</div>
		<div class="chck-green row">
			<div class="item">Adopt policies on overall cotton sustainability, and specifically on the key topics of HHPs, water, biodiversity, labour conditions, and recycling;</div>	
			<div class="item">Set public targets for more sustainable cotton sourcing by 2020, including the percentage of Better Cotton, organic, Fairtrade, CmiA and recycled cotton;</div>	
			<div class="item">Report transparently each year on policies, strategies and targets, as well as performance and progress.</div>	
		</div>
		<div class="row row-30">
			<div class="col-md-6">
				<p>In support of the above, companies should also:</p>
			</div>
		</div>
		<div class="chck-green row">
			<div class="item">Map cotton supply chains down to at least the country of cultivation;</div>	
			<div class="item">Join organisations such as BCI and/or Textile Exchange;</div>	
			<div class="item">Calculate the volume of cotton used by the business annually and make it public;</div>
			<div class="item">Seek advice from standards organisations, NGOs and peers in their sector.</div>	
			<div class="item">Develop a company-wide plan for applying policies and meeting sourcing targets, involving all relevant departments;</div>	
		</div>
