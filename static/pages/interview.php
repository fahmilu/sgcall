	<div class="container container-1240px">
		<h2>Marks & Spencer:<br /> "Understanding sustainable cotton options"</h2>
		<p class="intro-text">Marks & Spencer (M&S) is best known for their broad and ambitious Plan A for sustainability. For cotton, they've committed to source 70% from sustainable sources by 2020. To meet this commitment they use Better Cotton, Fairtrade Cotton, Organic Cotton and recycled cotton. </p>
		<div class="row">
			<div class="col-xs-6">
				<h4>A public commitment</h4>
				<p>"We use more cotton in our clothing than any other raw material", Phil Townsend, Sustainable Raw Material Specialist at M&S, explains. "A decade ago, with Plan A, we started making our products more sustainable. We realized that cotton has a significant environmental impact in terms of water, land use, chemicals and social issues. We felt it was our responsibility to make a solid, firm and critical commitment to sourcing more sustainable cotton. We made this target public as well, so we can be held accountable." </p>
			</div>
			<div class="col-xs-6">
				<h4>Senior management support</h4>
				<p>According to Townsend, it's absolutely critical to get buy-in from your senior management before you start sourcing sustainable cotton. "It shouldn't only be the responsibility of a small sustainability team, it has to be embedded throughout the company. For us, it helped to break our targets down to each of the business unit areas and further down to the individual product buying teams. This way, collaborators are not just playing in an overall target, but they know their own specific targets." </p>
			</div>
		</div>
	</div>
</section>
