			<div class="col-xs-6">
				<h4>A clear marketing plan</h4>
				<p>What’s Townsend’s advice for ensuring suppliers’ cooperation in the journey towards sustainable cotton? “Discuss price premiums with them in order to make costs transparent. In this way you can identify whether costs are fair or not. You need a lot of patience and good negotiation skills to reach good agreements on this. We also do a lot of in-country training with our suppliers, making them aware of why we’re asking them to source sustainably and also assessing their performance.”</p>
			</div>
			<div class="col-xs-6">
				<h4>Transparent price premiums</h4>
				<p>A big opportunity that M&S still needs to realize is tapping into customer awareness and driving more demand for sustainable cotton from consumers. “To achieve this we’re developing a clear marketing plan to engage with customers and make sure they understand our sustainable cotton story in a clear and concise way.”</p>
