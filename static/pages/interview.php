				<h4>Different cotton options</h4>
				<p>Phil Townsend emphasizes that when a company starts looking into sustainable cotton, they first need to understand the differences between the sustainable cotton standards and their relative benefits.</p>
				<p>“You then need to develop a strategy for this. Do you want to work with just one option, or do you want a balanced portfolio of different sustainable cotton products? Further down the line, you also need to train your sourcing and buying teams as each sustainable cotton works differently in that respect. Fairtrade and BCI have been great in supporting us with training needs. We also picked up a lot of training ourselves. You need to keep these costs in mind when you start your sustainable cotton journey.”</p>
