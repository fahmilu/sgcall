				<h4 class="title">Better Cotton Initiative (BCI)</h4>
				<p>Better Cotton represents the largest share of more sustainable cottons. In the 2015/16 season, 2.5 million MT of lint of Better Cotton lint was produced in 23 countries on five continents, including the cotton produced under benchmarked standards: 52,000 MT myBMP in Australia, 832,000 MT Algodão Brasileiro Responsável (ABR) in Brazil, and 280,779 MT CmiA. Better Cotton was produced by 1.5 million farmers on 3.5 million hectares of land.</p>
			</div>
			<div class="col-md-6">
				<p>Uptake of Better Cotton has increased steeply. In 2015, 251,000 MT of Better Cotton was actively sourced by brands and retailers. In 2016, this almost doubled to 461,000 MT sourced by 54 retailers. Share of uptake relative to production therefore increased from 13% to 18%. On spinner level 807,000 MT of cotton lint was sourced, amounting to 32% of the total volume produced.</p>
