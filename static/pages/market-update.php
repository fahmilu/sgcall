		<p class="clr-white">Recognition that uptake is essential in stimulating production and mainstreaming more sustainable cotton has spread widely across the industry. However, most cotton produced globally is still conventional cotton. And of all more sustainable cotton produced, 79% is not sourced actively by brands and retailers. The increase in uptake is welcome but the gap between supply and demand is still too wide. For more sustainable cotton to become the norm, both production and uptake need to increase significantly.</p>
