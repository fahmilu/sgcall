<section id="IssuesAndProblems" class="white-bg">
	<div class="container container-1240px">
		<div class="row">
			<div class="col-xs-6">
				<h4 class="title">Environmental issues</h4>
				<p>Environmental challenges in conventional cotton production include: </p>
				<ul class="checklist chck-green">
					<li>Overuse and misuse of pesticides, which has significant impacts on ecosystems, as well as the health of farmers and their communities;</li>
					<li>Overuse of synthetic fertilisers, which causes loss of soil fertility and soil acidification;</li>
					<li>Overuse of water – the global average water footprint of seed cotton is nearly 1.5 Olympic swimming pools per tonne.</li>
				</ul>
			</div>
			<div class="col-xs-6">
				<h4 class="title">Socio-economical problems</h4>
				<p>Socio-economic challenges in conventional cotton production include:</p>
				<ul class="checklist chck-green">
					<li>Poor working conditions, child labour and forced labour;</li>
					<li>High debts for farmers due to heavy use of costly inputs like pesticides and fertilisers;</li>
					<li>Low farmer incomes under constant threat from global market volatility;</li>
					<li>Gender inequality holding back inclusive development in cotton farming communities.</li>
				</ul>
			</div>
		</div>
	</div>
</section>
