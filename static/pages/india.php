<section id="Context" class="white-bg">
	<div class="container container-blockquote">
		<h4 class="title">context</h4>
		<div class="two-columns">
			<p>India is emerging as one of the most attractive retail destinations in the world but domestic players face stiff competition from international brands. </p>
			<p>Consumer purchasing choices are driven primarily by affordability and most Indian brands focus on addressing consumer needs, increasing market share and improving profitability.</p>
		</div>
		<hr />
		<div class="row row-30">
			<div class="col-md-6">
				<h6><i class="policy"></i>policy</h6>
				<p>Indian companies do not have clear cotton procurement policies or targets. Increasing levels of vendor engagement on water stewardship, chemical management and socially responsible labour practices in tier-1 and tier-2 of supply chains are hampered by a lack of technical know-how, capacity and awareness about ethical supply chains, even among larger businesses.</p>
			</div>
			<div class="col-md-6">
				<h6><i class="uptake"></i>uptake</h6>
				<p>Actual uptake of more sustainable cotton domestically is negligible. While the brands recognise the need to adopt sustainable business practices, most current efforts focus on manufacturing resource efficiency and social license to operate. A more holistic approach addressing the lifecycle of products and operations is yet to be widely adopted.</p>
			</div>
		</div>
		<div class="row row-30">
			<div class="col-md-6">
				<h6><i class="traceability"></i>Traceability</h6>
				<p>While two out of the top four brands publish sustainability reports, public disclosure on actual cotton consumption is limited. Most Indian companies struggle to establish traceability in their complex supply chains.</p>
			</div>
		</div>
	</div>
</section>
